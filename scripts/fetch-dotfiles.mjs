#!/usr/bin/env node
// Fetches the tree of the dotfiles repo from GitHub and writes the result to
// src/constants/dotfiles.constant.ts. Runs unauthenticated; only public-repo
// endpoints are used. No env vars or tokens required.

import { writeFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const REPO_OWNER = 'bopeng95';
const REPO_NAME = 'dotfiles';
const REPO_URL = `https://github.com/${REPO_OWNER}/${REPO_NAME}`;

const __dirname = dirname(fileURLToPath(import.meta.url));
const OUTPUT_PATH = resolve(
  __dirname,
  '..',
  'src/constants/dotfiles.constant.ts',
);

const API_HEADERS = {
  Accept: 'application/vnd.github+json',
  'X-GitHub-Api-Version': '2022-11-28',
};

async function fetchJson(url) {
  const response = await fetch(url, { headers: API_HEADERS });
  if (!response.ok) {
    const isRateLimited =
      response.status === 403 &&
      response.headers.get('x-ratelimit-remaining') === '0';
    const message = isRateLimited
      ? 'GitHub API rate limit exceeded (60 req/hr unauthenticated). Wait and retry.'
      : `GitHub API ${response.status} ${response.statusText} for ${url}`;
    throw new Error(message);
  }
  return response.json();
}

async function getDefaultBranch() {
  const repo = await fetchJson(
    `https://api.github.com/repos/${REPO_OWNER}/${REPO_NAME}`,
  );
  return repo.default_branch;
}

async function getTree(branch) {
  const tree = await fetchJson(
    `https://api.github.com/repos/${REPO_OWNER}/${REPO_NAME}/git/trees/${branch}?recursive=1`,
  );
  if (tree.truncated) {
    throw new Error(
      'Tree response was truncated by GitHub — repo too large for a single call. Refusing to write a partial tree.',
    );
  }
  return tree.tree;
}

async function getChezmoiSourceRoot(branch) {
  const response = await fetch(
    `https://raw.githubusercontent.com/${REPO_OWNER}/${REPO_NAME}/${branch}/.chezmoiroot`,
  );
  if (!response.ok) return null;
  return (await response.text()).trim();
}

const CHEZMOI_ATTRIBUTE_PREFIX =
  /^(?:encrypted_|private_|readonly_|executable_|empty_|exact_|literal_|symlink_)+/;
const LICENSE_PATTERN = /^LICENSE(?:[-.].*)?$/i;
const README_PATTERN = /^README(?:[-.].*)?$/i;
const EXCLUDED_EXTENSION = /\.(?:png|xml|tmtheme)$/i;

function shouldExcludePath(path) {
  const basename = path.split('/').pop() ?? '';
  const prefixMatch = basename.match(CHEZMOI_ATTRIBUTE_PREFIX);
  const prefixChain = prefixMatch?.[0] ?? '';
  if (prefixChain.includes('readonly_')) return true;
  const stripped = basename.replace(CHEZMOI_ATTRIBUTE_PREFIX, '');
  return (
    LICENSE_PATTERN.test(stripped) ||
    README_PATTERN.test(stripped) ||
    EXCLUDED_EXTENSION.test(stripped)
  );
}

function buildNestedTree(flatEntries, sourceRoot) {
  // Names are intentionally rendered as-is from the repo (e.g. `dot_aliases`,
  // `readonly_LICENSE`). The Dotfiles section explains the chezmoi naming
  // convention to the reader.
  const root = new Map();
  const rootPrefix = sourceRoot ? `${sourceRoot}/` : '';
  const blobs = flatEntries
    .filter((entry) => entry.type === 'blob')
    .filter((entry) => !sourceRoot || entry.path.startsWith(rootPrefix))
    .filter((entry) => !shouldExcludePath(entry.path))
    .sort((a, b) => a.path.localeCompare(b.path));

  for (const blob of blobs) {
    const fullPath = blob.path;
    const relativePath = sourceRoot
      ? fullPath.slice(rootPrefix.length)
      : fullPath;
    const parts = relativePath.split('/');
    let cursor = root;
    for (let i = 0; i < parts.length; i++) {
      const rawPart = parts[i];
      const isLeaf = i === parts.length - 1;
      if (!cursor.has(rawPart)) {
        cursor.set(rawPart, {
          name: rawPart,
          path: isLeaf ? fullPath : undefined,
          size: isLeaf ? (blob.size ?? 0) : undefined,
          children: isLeaf ? undefined : new Map(),
        });
      }
      if (!isLeaf) {
        cursor = cursor.get(rawPart).children;
      }
    }
  }

  return mapToPlain(root);
}

function mapToPlain(map) {
  return [...map.values()].map((node) => {
    if (node.children) {
      return { name: node.name, children: mapToPlain(node.children) };
    }
    return { name: node.name, path: node.path, size: node.size };
  });
}

function renderFile(tree) {
  return `// AUTO-GENERATED by scripts/fetch-dotfiles.mjs — do not edit by hand.
// Run \`npm run fetch-dotfiles\` to refresh. Runs automatically before \`npm run build\`.

import type { DotfileNode } from '@/types';

export const DOTFILES_REPO = '${REPO_URL}';

export const dotfiles: DotfileNode[] = ${JSON.stringify(tree, null, 2)};
`;
}

async function main() {
  const branch = await getDefaultBranch();
  const [flatTree, sourceRoot] = await Promise.all([
    getTree(branch),
    getChezmoiSourceRoot(branch),
  ]);
  const nestedTree = buildNestedTree(flatTree, sourceRoot);
  writeFileSync(OUTPUT_PATH, renderFile(nestedTree), 'utf8');
  const rootedAt = sourceRoot ? ` (chezmoi root: ${sourceRoot}/)` : '';
  console.log(
    `Wrote ${OUTPUT_PATH} — ${countNodes(nestedTree)} files from ${REPO_OWNER}/${REPO_NAME}@${branch}${rootedAt}`,
  );
}

function countNodes(nodes) {
  let count = 0;
  for (const node of nodes) {
    if (node.children) count += countNodes(node.children);
    else count += 1;
  }
  return count;
}

main().catch((err) => {
  console.error(err.message);
  process.exit(1);
});
